#include <cstdio>
using namespace std;
int N, M;

int main() {
    int T;
    scanf("%d", &T);
    while(T--){
        scanf("%d %d", &N, &M);
        while(M--){
            int a, b;
            scanf("%d %d", &a, &b);
        }
        // The answer is always N-1
        printf("%d\n", N-1);
    }
}